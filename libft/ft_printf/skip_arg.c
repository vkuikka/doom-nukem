char	*ft_skiparg(char *arg)
{
	if (*arg == '%')
		return (arg);
	while (*arg && *arg != 'd' && *arg != 'i' && *arg != 'o' && *arg != 'u'
		&& *arg != 'f' && *arg != 'x' && *arg != 'X' && *arg != 'c'
		&& *arg != 's' && *arg != 'p' && *arg != '%')
		arg++;
	return (arg);
}
